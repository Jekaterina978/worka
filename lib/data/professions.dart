  static List<String> flat() => groups.values.expand((e) => e).toList();

  static List<String> suggest(String query, {int limit = 8}) {
    final q = query.trim().toLowerCase();
    if (q.isEmpty) return const [];

    final all = flat();
    final starts = <String>[];
    final contains = <String>[];

    for (final p in all) {
      final t = p.toLowerCase();
      if (t.startsWith(q)) {
        starts.add(p);
      } else if (t.contains(q)) {
        contains.add(p);
      }
    }
    return [...starts, ...contains].take(limit).toList();
  }
